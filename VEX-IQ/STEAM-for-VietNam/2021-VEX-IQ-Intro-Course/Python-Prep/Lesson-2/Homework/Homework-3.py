"""
Viết một hàm
- Nhận vào tham số là 1 danh sách các chuỗi
- Trả về chuỗi có độ dài lớn nhất
"""
numbers = [100, 90, 200]

for number in numbers:
    the_largest_number = 0
    if number > the_largest_number:
        the_largest_number = number

print(f"The largest number is {the_largest_number}.")
